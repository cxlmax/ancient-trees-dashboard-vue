import { transfromMapGeoJSON } from "@/mini3d";
import { Assets } from "./assets.js";
import emitter from "@/utils/emitter";
import gsap from "gsap";
import infoData from "./map/infoData.js";
import barData from "./map/barData.js";
import * as pathPointData from "./map/pathPointData.js";
import { sleep } from "@/utils";
const assets = shallowRef(null);
const mapSceneRef = ref(null);
const state = reactive({
  // 进度
  progress: 0,
  // 当前顶部导航索引
  activeIndex: "1",
  // 卡片统计数据
  totalView: [
    {
      icon: "xiaoshoujine",
      zh: "2024年生产总值",
      en: "Gross Domestic Product in 2024",
      value: 31500,
      unit: "亿元",
    },
    {
      icon: "zongxiaoliang",
      zh: "2024年常驻人数",
      en: "resident population in 2024",
      value: 15000,
      unit: "万人",
    },
  ],
  bottomMenuIndex: 0,
  bottomMenuStatus: {
    bar: true,
    point: false,
    path: false,
    hotmap: false,
  },
});
onMounted(async () => {
  // 监听地图播放完成，执行事件
  emitter.$on("mapPlayComplete", handleMapPlayComplete);
  // handleMapPlayComplete()
  await sleep(1000);
  // 初始化资源
  initAssets(async () => {
    // 加载地图
    emitter.$emit("loadMap", assets.value);
    // 隐藏loading
    await hideLoading();

    // 播放场景
    mapSceneRef.value.play();
  });
});
onBeforeUnmount(() => {
  emitter.$off("mapPlayComplete", handleMapPlayComplete);
});
// 初始化加载资源
function initAssets(onLoadCallback) {
  assets.value = new Assets();
  // 资源加载进度
  let params = {
    progress: 0,
  };
  assets.value.instance.on("onProgress", (path, itemsLoaded, itemsTotal) => {
    let p = Math.floor((itemsLoaded / itemsTotal) * 100);
    gsap.to(params, {
      progress: p,
      onUpdate: () => {
        state.progress = Math.floor(params.progress);
      },
    });
  });
  // 资源加载完成
  assets.value.instance.on("onLoad", () => {
    onLoadCallback && onLoadCallback();
  });
}

// 隐藏loading
async function hideLoading() {
  return new Promise((resolve, reject) => {
    let tl = gsap.timeline();
    tl.to(".loading-text span", {
      y: "200%",
      opacity: 0,
      ease: "power4.inOut",
      duration: 2,
      stagger: 0.2,
    });
    tl.to(
      ".loading-progress",
      { opacity: 0, ease: "power4.inOut", duration: 2 },
      "<"
    );
    tl.to(
      ".loading",
      {
        opacity: 0,
        ease: "power4.inOut",
        onComplete: () => {
          resolve();
        },
      },
      "-=1"
    );
  });
}

function handleMenuSelect(index) {
  state.activeIndex = index;
}
// 地图开始动画播放完成
function handleMapPlayComplete() {
  let tl = gsap.timeline({ paused: false });
  let leftCards = gsap.utils.toArray(".left-card");
  let rightCards = gsap.utils.toArray(".right-card");
  let countCards = gsap.utils.toArray(".count-card");
  let leftZsline = document.querySelector(".large-screen-map-left-zsline");
  let rightZsline = document.querySelector(".large-screen-map-right-zsline");
  tl.addLabel("start", 0.5);
  tl.addLabel("menu", 0.5);
  tl.addLabel("card", 1);
  tl.addLabel("countCard", 3);

  tl.fromTo(
    leftZsline,
    { x: 200, opacity: 0 },
    { x: 0, opacity: 1, duration: 1, ease: "power4.out" },
    "start"
  );
  tl.fromTo(
    rightZsline,
    { x: -200, opacity: 0 },
    { x: 0, opacity: 1, duration: 1, ease: "power4.out" },
    "start"
  );
  tl.to(
    ".m-header",
    { y: 0, opacity: 1, duration: 1.5, ease: "power4.out" },
    "start"
  );
  tl.to(
    ".bottom-tray",
    { y: 0, opacity: 1, duration: 1.5, ease: "power4.out" },
    "start"
  );

  tl.to(
    leftCards,
    { x: 0, opacity: 1, stagger: 0.2, duration: 1.5, ease: "power4.out" },
    "card"
  );
  tl.to(
    rightCards,
    { x: 0, opacity: 1, stagger: 0.2, duration: 1.5, ease: "power4.out" },
    "card"
  );
  tl.to(
    countCards,
    {
      y: 0,
      opacity: 1,
      stagger: 0.2,
      duration: 1.5,
      ease: "power4.out",
    },
    "card"
  );
}
// 创建柱状图
function createBar() {
  let data = barData.filter((item, index) => index < 17);
  emitter.$emit("createBar", data);
}
// 创建动态点位
function createPoint() {
  emitter.$emit("createPoint", infoData);
}
// 创建路径
function createPath() {
  // 请求路径，渲染线条
  let path1 = pathPointData.path1;
  let path2 = pathPointData.path2;
  // 创建路径
  emitter.$emit("createPath", path1);
  emitter.$emit("createPath", path2);
}
// 创建热力图
function createHeatmap() {
  emitter.$emit("createHeatmap");
}
function changeMenu(type) {
  state.bottomMenuStatus[type] = !state.bottomMenuStatus[type];
 
  // 销毁柱状图
  // emitter.$emit("destroyBar");
  // 销毁动态点位
  // emitter.$emit("destroyPoint");
  // 销毁路径
  // emitter.$emit("destroyPath");
  // 销毁热力图
  // emitter.$emit("destroyHeatmap");
  const destroyActions = {
    bar: () => {
      emitter.$emit("destroyBar");
    },
    point: () => {
      emitter.$emit("destroyPoint");
    },
    path: () => {
      emitter.$emit("destroyPath");
    },
    hotmap: () => {
      emitter.$emit("destroyHeatmap");
    },
  };
  const createActions = {
    bar: () => {
      createBar();
    },
    point: () => {
      createPoint();
    },
    path: () => {
      createPath();
    },
    hotmap: () => {
      createHeatmap();
    },
  };
  // 创建
  if (state.bottomMenuStatus[type]) {
    if (createActions[type]) {
      createActions[type]();
    }
  } else {
    // 销毁
    if (destroyActions[type]) {
      destroyActions[type]();
    }
  }
}
</script>

